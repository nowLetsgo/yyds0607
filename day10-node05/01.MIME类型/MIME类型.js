/* 
    Content-Type用于指示资源的MIME类型，告诉客户端实际上服务端返回的内容的类型

    MIME类型也叫作媒体类型，表示文档文件的内容格式

    常见的MIME类型：
        text/plain: 普通文本
        text/css: css文件
        text/html: html文件
        application/javascript: js文件
        application/x-www-form-urlencoded: form表单类型
        image/图片格式: 图片
        video/视频格式: 视频
        audio/音频格式: 音频

*/